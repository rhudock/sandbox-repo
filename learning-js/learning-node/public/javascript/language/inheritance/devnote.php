      <p>General</p>
      <p>The jQuery library, and virtually all of its plugins are constrained within the jQuery namespace. As a general rule, &quot;global&quot; objects are stored inside the jQuery namespace as well, so you shouldn't get a clash between jQuery and any other library (like Prototype, MooTools, or YUI).</p>
      <p>That said, there is one caveat: By default, jQuery uses &quot;$&quot; as a shortcut for &quot;jQuery&quot; </p>
